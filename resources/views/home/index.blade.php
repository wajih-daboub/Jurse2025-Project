            <p>
                L'<a class="text-decoration-underline" href="http://jurse.org/" target="_blank"><b>Événement de
                        Télédétection Urbaine Conjointe (JURSE)</b></a> est un
                forum d'excellence où chercheurs, praticiens et étudiants présentent, partagent et discutent de leurs
                dernières découvertes et résultats.
                Une version très dynamique du JURSE organisée par le Laboratoire de Télédétection de FORTH à Héraklion,
                Grèce en
                mai 2025.
            </p>

            <p>
                <b>
                    Cet événement s'engage à introduire des méthodologies innovantes et des ressources technologiques
                    récemment
                    employées pour étudier les multiples aspects de l'environnement urbain grâce aux données de
                    télédétection orbitale et aérienne.
                </b>
            </p>

            <p>
                Des sujets émergents tels que de nouvelles méthodes pour la classification de la couverture et de
                l'utilisation des terres urbaines avec une discrimination détaillée des cibles urbaines,
                la modélisation 3D des bâtiments urbains, la prévision et l'évaluation de l'impact des dangers naturels
                et anthropiques dans les zones urbaines, les études sociales urbaines,
                l'écologie urbaine, la climatologie urbaine, ainsi que la fusion de données, les algorithmes et les
                techniques pour l'interprétation des données d'observation de la Terre ont été abordés.
            </p>

